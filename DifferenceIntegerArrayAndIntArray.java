
class  DifferenceIntegerArrayAndIntArray{
	
	public static void main(String [] args){
		
		
		Integer [] a = new Integer[10];
		
		int [] b = new int[10];
		
		for(Integer i:a){
			System.out.println(i);
		}
		
		for(int i:b){
			System.out.println(i);
		}
		
		
		
		
		
	}
	
}


