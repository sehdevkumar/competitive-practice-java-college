class MultiDimention{
	
	public static void main(String [] args){
		int TwoDimensinal[][] = {
			{1,2,3},
			{2,4,11},
			{2,3,8},
				
		};
		
		for(int i=0;i<TwoDimensinal.length;i++){
			for(int j=0;j<TwoDimensinal[i].length;j++){
				System.out.print(TwoDimensinal[i][j]+" ");
				
			}
			
		}
		
		
		
	}
	
	
}